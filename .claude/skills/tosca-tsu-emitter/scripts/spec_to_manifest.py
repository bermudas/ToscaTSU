#!/usr/bin/env python3
"""
spec_to_manifest.py — Playwright .spec.ts → parse_tsu-shaped manifest JSON.

Used by gen_tsu.py via `--spec`. Walks the spec tree-sitter AST, cross-
references locator texts against a `pages/*.page.ts` catalog, reconstructs
folder/block/control-flow structure from comments, and emits a manifest
that matches the schema parse_tsu produces.

Three input shapes the parser tries to handle:

1. **Inline with comments** (auto-generated by parse_tsu, the round-trip path):
   - `// CDE | Cookie window`            → module name marker
   - `// Click Submit btn`               → step name
   - `// ── Folder Name`                 → folder boundary
   - `// [if Foo] condition: 1=="1"`     → control flow open
   - `// [Then]` / `// [Else]` / `// [Loop]` → branch open
   - `// [end if Foo]`                   → control flow close
   - `// TBox Wait: 3000 ms`             → synthesizes wait step
   - `await page.locator(…).click()`     → action

2. **POM-style** (`pageObj.field.action()`): the field reference is resolved
   through the page-objects catalog (locator text + module + attribute name).

3. **Bare inline** (no comments, no POM imports): each locator is matched
   against the page-objects catalog by source text; if not found, the step
   is emitted with a synthetic module name so the user can hand-fix.

Public entry point:
    build_manifest_from_spec(spec_path, pages_dir, base_manifest=None)
        → dict in parse_tsu manifest format
"""

import re
from pathlib import Path
from collections import defaultdict
import tree_sitter as ts
import tree_sitter_typescript as tsts


# ──────────────────────────────────────────────────────────────────────────────
# Tree-sitter setup
# ──────────────────────────────────────────────────────────────────────────────
_LANG = ts.Language(tsts.language_typescript())
_PARSER = ts.Parser(_LANG)


def _src(node, source: bytes) -> str:
    """Return the literal source text of a node."""
    return source[node.start_byte:node.end_byte].decode('utf-8', errors='replace')


def _children_by_type(node, type_name: str):
    return [c for c in node.children if c.type == type_name]


# ──────────────────────────────────────────────────────────────────────────────
# Page-object parser  →  catalog
# ──────────────────────────────────────────────────────────────────────────────
# Each catalog entry: {
#     'class_name':  'LoginPagePage',
#     'module_name': 'Login page',          # derived; user can override via JSON map
#     'fields':      [{'name': 'email_address', 'locator_text': 'page.locator(\'#signInName\')'}],
# }
#
# We also build a reverse index normalized_locator_text → (class_name, field_name)
# so that the spec parser can match `page.locator('#signInName')` back to
# (LoginPagePage, email_address).

_FIELD_ASSIGN_QUERY = _LANG.query("""
(assignment_expression
  left: (member_expression
    object: (this)
    property: (property_identifier) @field)
  right: (_) @rhs) @assign
""")


def _camel_to_words(name: str) -> str:
    """LoginPagePage → 'Login Page'  (drop trailing 'Page' if present)."""
    if name.endswith('Page'):
        name = name[:-4]
    parts = re.findall(r'[A-Z][a-z0-9]*|[a-z0-9]+', name)
    return ' '.join(p.capitalize() for p in parts) if parts else name


def _normalize_locator_text(text: str) -> str:
    """Collapse whitespace + line continuations so two formattings match."""
    t = re.sub(r'\s+', ' ', text)
    t = re.sub(r'\s*\.\s*', '.', t)
    t = re.sub(r'\s*,\s*', ', ', t)
    return t.strip()


def parse_pages_dir(pages_dir: Path):
    """Walk a pages/ directory; return list of {class_name, module_name, fields[]}.

    Plus a reverse map: { normalized_locator_text → (class_name, field_name) }."""
    catalog = []
    rev_index = {}

    for ts_path in sorted(pages_dir.glob('*.page.ts')):
        source = ts_path.read_bytes()
        tree = _PARSER.parse(source)
        root = tree.root_node
        # find each class declaration
        for cls_node in _walk_find(root, 'class_declaration'):
            name_node = cls_node.child_by_field_name('name')
            if not name_node: continue
            class_name = _src(name_node, source)
            # Constructor body: find method with name='constructor'
            ctor = _find_constructor(cls_node)
            fields = []
            if ctor is not None:
                body = ctor.child_by_field_name('body')
                if body:
                    matches = _FIELD_ASSIGN_QUERY.matches(body)
                    for _id, captures in matches:
                        field_node = captures.get('field', [None])[0]
                        rhs_node = captures.get('rhs', [None])[0]
                        if field_node is None or rhs_node is None: continue
                        fname = _src(field_node, source)
                        if fname == 'page':  # skip the 'this.page = page;' binding
                            continue
                        rhs_text = _src(rhs_node, source)
                        fields.append({'name': fname, 'locator_text': rhs_text})
                        rev_index[_normalize_locator_text(rhs_text)] = (class_name, fname)
                        # Also index the .or(...) right-hand-side primary alone, so that
                        # specs that drop the .or() fallback can still match.
                        primary_only = re.split(r'\s*\.\s*or\s*\(', rhs_text)[0]
                        rev_index.setdefault(_normalize_locator_text(primary_only),
                                              (class_name, fname))
            catalog.append({
                'class_name':  class_name,
                'module_name': _camel_to_words(class_name),
                'pages_file':  str(ts_path),
                'fields':      fields,
            })
    return catalog, rev_index


def _walk_find(node, type_name: str):
    """DFS yield all descendants of a given type."""
    if node.type == type_name:
        yield node
    for c in node.children:
        yield from _walk_find(c, type_name)


def _find_constructor(class_node):
    body = class_node.child_by_field_name('body')
    if not body: return None
    for c in body.children:
        if c.type == 'method_definition':
            name = c.child_by_field_name('name')
            if name and name.type == 'property_identifier':
                if name.text == b'constructor':
                    return c
    return None


# ──────────────────────────────────────────────────────────────────────────────
# Spec parser  →  flat node stream (manifest.steps shape)
# ──────────────────────────────────────────────────────────────────────────────
# We walk the spec linearly: every comment gets read in order, every
# `await page.<...>.<action>(...)` becomes a tentative action. A comment-state
# machine emits folder/block/if/then/else/loop/step boundary nodes.

# Structured @tosca markers (emitted by parse_tsu.py for unambiguous round-trip).
# These are the PRIMARY source of structural truth; legacy heuristics below are
# fallback for hand-written specs without markers.
TOSCA_MARKERS = [
    ('folder_open',  re.compile(r'^//\s*@tosca\s+folder:\s*"((?:\\.|[^"])*)"\s*$')),
    ('folder_close', re.compile(r'^//\s*@tosca\s+/folder\s*$')),
    ('block_open',   re.compile(r'^//\s*@tosca\s+block:\s*"((?:\\.|[^"])*)"\s*$')),
    ('block_close',  re.compile(r'^//\s*@tosca\s+/block\s*$')),
    ('if_open',      re.compile(r'^//\s*@tosca\s+if:\s*"((?:\\.|[^"])*)"\s+cond="((?:\\.|[^"])*)"(?:\s+verdict=(True|False))?\s*$')),
    ('if_close',     re.compile(r'^//\s*@tosca\s+/if\s*$')),
    ('branch_open',  re.compile(r'^//\s*@tosca\s+(then|else|loop)\s*$')),
    ('branch_close', re.compile(r'^//\s*@tosca\s+/branch\s*$')),
    ('step_marker',  re.compile(r'^//\s*@tosca\s+step:\s*"((?:\\.|[^"])*)"\s+module="((?:\\.|[^"])*)"\s*$')),
    ('wait_marker',  re.compile(r'^//\s*@tosca\s+wait:\s*(\d+)(?:\s+name="((?:\\.|[^"])*)")?\s*$')),
    ('buffers',      re.compile(r'^//\s*@tosca\s+buffers:\s*(.*)$')),
]

# Legacy heuristic patterns (used only when no @tosca markers are present in
# the spec — i.e. for hand-written specs). They're noisier but degrade
# gracefully.
LEGACY_PATTERNS = [
    ('folder',         re.compile(r'^//\s*──\s*(.+?)\s*$')),
    ('legacy_if_open', re.compile(r'^//\s*\[if\s+(.+?)\]\s*condition:\s*(.*?)(?:\s*→\s*(True|False))?\s*$')),
    ('legacy_branch',  re.compile(r'^//\s*\[(Then|Else|Loop)\]\s*$')),
    ('legacy_if_close',re.compile(r'^//\s*\[end if\s+(.+?)\]\s*$')),
    ('legacy_wait',    re.compile(r'^//\s*TBox Wait:\s*(\d+)\s*ms\s*$')),
    ('encrypted',      re.compile(r'^//\s*.+Tosca-encrypted')),
    ('step_or_module', re.compile(r'^//\s*(.+?)\s*$')),
]


def _unquote(s: str) -> str:
    """Reverse the _mq() escape used by parse_tsu's marker emitter."""
    return s.replace('\\"', '"').replace('\\\\', '\\')


